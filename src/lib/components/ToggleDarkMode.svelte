<script lang="ts">
  import { onMount } from 'svelte';
  import { prefersDarkTheme, getTheme, storageKey } from "$lib/helpers/theme";

  let currentTheme: string = getTheme();
  
  const toggleTheme = () => {
    const stored = localStorage.getItem(storageKey);

    if (stored) {
      localStorage.removeItem(storageKey);
    } else {
      localStorage.setItem(storageKey, prefersDarkTheme() ? 'light' : 'dark')
    }

    applyTheme();
  }

  const applyTheme = () => {
    currentTheme = getTheme();

    if (currentTheme === 'dark') {
      document.body.classList.remove('light');
      document.body.classList.add('dark');
    } else {
      document.body.classList.remove('dark');
      document.body.classList.add('light');
    }
  }
  
  onMount(() => {
    applyTheme();
    window.matchMedia("(prefers-color-scheme: dark)").addEventListener('change', applyTheme);
    
    return () =>  window.matchMedia("(prefers-color-scheme: dark)").removeEventListener('change', applyTheme);
  })

</script>

<button on:click={toggleTheme}>
  {#if currentTheme === "dark"}
    <svg stroke="currentColor" fill="currentColor" stroke-width="0" viewBox="0 0 24 24" height="30" width="30" xmlns="http://www.w3.org/2000/svg"><g><path fill="none" d="M0 0h24v24H0z"></path><path d="M8 12h2v2H4v-2h2a6 6 0 1 1 6 6v-2a4 4 0 1 0-4-4zm-2 8h9v2H6v-2zm-4-4h8v2H2v-2zm9-15h2v3h-2V1zM3.515 4.929l1.414-1.414L7.05 5.636 5.636 7.05 3.515 4.93zM16.95 18.364l1.414-1.414 2.121 2.121-1.414 1.414-2.121-2.121zm2.121-14.85l1.414 1.415-2.121 2.121-1.414-1.414 2.121-2.121zM23 11v2h-3v-2h3z"></path></g></svg>
  {/if}
  {#if currentTheme === "light"}
    <svg stroke="currentColor" fill="currentColor" stroke-width="0" viewBox="0 0 24 24" height="30" width="30" xmlns="http://www.w3.org/2000/svg"><g><path fill="none" d="M0 0h24v24H0z"></path><path fill-rule="nonzero" d="M10 7a7 7 0 0 0 12 4.9v.1c0 5.523-4.477 10-10 10S2 17.523 2 12 6.477 2 12 2h.1A6.979 6.979 0 0 0 10 7zm-6 5a8 8 0 0 0 15.062 3.762A9 9 0 0 1 8.238 4.938 7.999 7.999 0 0 0 4 12z"></path></g></svg>
  {/if}
</button>

